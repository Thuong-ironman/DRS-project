t43 = theta ^ 2;
t44 = t43 ^ 2;
t47 = rad_to_deg ^ 2;
t48 = t47 ^ 2;
t63 = v ^ 2;
force = 0.1e1 / t4 * t63 * (t47 * rad_to_deg * t43 * theta * kfit20 + t48 * rad_to_deg * t44 * theta * kfit22 + kfit18 * theta * rad_to_deg + t47 * t43 * kfit19 + t48 * t44 * kfit21 + kfit17) * t42 / 0.2e1;

end
